void wait(void)
{
	volatile int w;
	for (w = 0; w < 1000000; w++);
}

void print_hex(unsigned int value)
{
	const char *hex = "0123456789ABCDEF";
	int i;

	for (i = 0; i < 8; i++)
	{
		putchar(hex[value >> 28u]);
		value <<= 4;
	}
}

void print_hex16(unsigned int value)
{
	const char *hex = "0123456789ABCDEF";
	int i;

	for (i = 0; i < 4; i++)
	{
		putchar(hex[value >> 12u]);
		value <<= 4;
	}
}

void print_hex8(unsigned char value)
{
	const char *hex = "0123456789ABCDEF";

	putchar(hex[value >> 4]);
	putchar(hex[value & 0xF]);
}

void dump(const unsigned char *addr)
{
	int i;
	for (i = 0; i < 256; i++)
	{
		if (i % 16 == 0)
		{
			print_hex((int)&addr[i]);
			print("  ");
		}
		print_hex8(addr[i]);
		print(" ");
		if (i % 16 == 15)
			print("\n");
	}
}

unsigned int counters[16];
int envelope[16];
int envelope_part[16];

int main(void)
{
	int i;
	unsigned int last_time = 0;
	int toggle = 0;
	int fn, block;
	int period;
	int sin;
	int play_index;

	int acc, val;
	int wave;
	int angle;
	int harmonic;
	int attack, decay, sustain, release;
	int volume;
	int feedback;

	int rhythm;

	memset(counters, 0, sizeof(counters));
	memset(envelope, 0, sizeof(envelope));
	memset(envelope_part, 0, sizeof(envelope_part));

	print("YM3812 RISC-V\n");

	for (;;)
	{
		do
		{
			for (play_index = 0; play_index < 16; play_index++)
			{
				if ((r[0xB0 + play_index] & 0x20) == 0)
				{
					if (envelope_part[play_index] > 0)
						envelope_part[play_index] = 0;
				}
			}
		} while (*timer == last_time);
		last_time = *timer;

		acc = 2048;

		rhythm = r[0xBD] & 0x20 ? 1 : 0;

		for (i = 0; i < (rhythm ? 6 : 9); i++)
		{
			fn = r[0xa0 + i];
			block = r[0xb0 + i];
			harmonic = r[0x20 + i] & 15;
			wave = r[0xE0 + i] & 3;

			attack = r[0x60 + i] >> 4;
			decay = r[0x60 + i] & 0x0F;
			sustain = r[0x80 + i] >> 4;
			release = r[0x80 + i] & 0x0F;

			volume = 64;//(r[0x40 + i] & 0x3F) + 1;

			feedback = (r[0xC0 + i] >> 1) & 0x07;

			// f ffff ffff fbbb

			period = fn;
			period |= (block & 0x03) << 8;
			period += 32;
			period <<= (block >> 2) & 0x07;

			switch (harmonic)
			{
				case 0:
					period >>= 1;
					break;
				case 11:
					period *= 10;
					break;
				case 13:
					period *= 12;
					break;
				case 14:
					period *= 15;
					break;
				default:
					period *= harmonic;
					break;
			}

			if (i >= 3 && i <= 5)
			{
				if (r[0xC0 + i - 3] & 0x01)
				{
					val = envelope[i - 1];
					period = ((period >> 8) * val) >> 8;
					/*
					if (val & 0x8000)
						period = period >> 2;
					else if (val & 0x4000)
						period = period >> 1;
					else if (val & 0x2000)
						;
					else if (val & 0x1000)
						period = period << 1;
					else if (val & 0x800)
						period = period << 2;
					else if (val & 0x400)
						period = period << 3;
					else if (val & 0x200)
						period = period << 4;
					else
						period = period << 5;
					*/
				}
			}

			if (block & 0x20)
				counters[i] += period;
			else
				counters[i] = 0;

			angle = (counters[i] >> 9) & 0xFF;

			sin = sine_wave[angle];
			if (sin & 0x80)
				sin |= 0xFFFFFF00;

			switch (wave)
			{
				case 1:
					if (sin < 0)
						sin = 0;
					break;
				case 2:
					if (sin < 0)
						sin = -sin;
					break;
				case 3:
					if (sin < 0)
						sin = -sin;
					if (angle & 0x40)
						sin = 0;
					break;
			}

			switch (envelope_part[i])
			{
				case 0:
					envelope[i] += (attack + 1) * 1024;
					if (envelope[i] >= 65535)
					{
						envelope[i] = 65535;
						envelope_part[i] = 1;
					}
					break;
				case 1:
					envelope[i] -= (decay + 1) * 512;
					if (envelope[i] <= (0xFFFF >> sustain))
					{
						envelope[i] = 0xFFFF >> sustain;
						envelope_part[i] = 2;
					}
					break;
				case 2:
					if ((r[0x20 + i] & r[0xB0 + i] & 0x20) == 0)
					{
						envelope_part[i] = 3;
					}
					break;
				case 3:
					envelope[i] -= (release + 1) * 512;
					if (envelope[i] <= 0)
					{
						envelope[i] = 0;
						envelope_part[i] = 4;
					}
					break;
			}

			sin = sin * (envelope[i] >> 8);
			sin >>= 8;

			if (i > 2 || ((r[0xC0 + i] & 0x01) == 0))
				acc += (sin * volume) >> 6;
		}

		*gpio = acc >> 4;
	}
}
